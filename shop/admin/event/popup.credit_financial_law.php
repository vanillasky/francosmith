<div>&nbsp; ② 신용카드가맹점은 신용카드로 거래를 할 때마다 그 신용카드를 본인이 정당하게 사용하고 있는지를 확인하여야 한다.</div>
<div>&nbsp; ③ 신용카드가맹점은 가맹점수수료를 신용카드회원이 부담하게 하여서는 아니 된다.</div>
<div>&nbsp; ④ 신용카드가맹점은 다음 각 호의 어느 하나에 해당하는 행위를 하여서는 아니 된다. 다만, 결제대행업체의 경우에는 제1호·제4호 및 제5호를 적용하지 아니하고, 수납대행가맹점의 경우에는 제3호·제5호(제2조제5호의2에 따라 대행하는 행위에 한한다)를 적용하지 아니한다. &nbsp;
	<span style="COLOR: #024fce;">&lt;개정 2010.3.12&gt;</span></div>
<div>&nbsp;&nbsp; 1. 물품의 판매 또는 용역의 제공 등이 없이 신용카드로 거래한 것처럼 꾸미는 행위</div>
<div>&nbsp;&nbsp; 2. 신용카드로 실제 매출금액 이상의 거래를 하는 행위</div>
<div>&nbsp;&nbsp; 3. 다른 신용카드가맹점의 명의(名義)를 사용하여 신용카드로 거래하는 행위</div>
<div>&nbsp;&nbsp; 4. 신용카드가맹점의 명의를 타인에게 빌려주는 행위</div>
<div>&nbsp;&nbsp; 5. 신용카드에 의한 거래를 대행하는 행위</div>
<div>&nbsp; ⑤ 결제대행업체는 물품의&nbsp;판매 또는 용역의 제공 등을 하는 자의 신용정보 및 신용카드에 의한 거래를 대행한 내용을 &nbsp;&nbsp;신용카드업자에게 제공하는 등&nbsp;대통령령으로 정하는&nbsp;사항을 지켜야 한다.</div>
